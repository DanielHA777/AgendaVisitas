package Model;

public class Residente {
private String nome;
private int id;
private String quarto;

public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getQuarto() {
	return quarto;
}
public void setQuarto(String quarto) {
	this.quarto = quarto;
}

}
